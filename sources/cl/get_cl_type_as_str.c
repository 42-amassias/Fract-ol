const char	*get_cl_type_as_str(
				t_cl_arg_type type)
{
	size_t	i;

	i = 0;
	while (i < CL_TYPE_COUNT)
		if (g_cl_types[i++].internal_type == type)
			return (g_cl_types[i - 1].str_type);
	return ("Unknown");
}
