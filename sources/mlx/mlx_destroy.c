int	mlx_destroy(
		t_mlx *mlx)
{
	if (mlx->mlx == NULL)
		return (EXIT_SUCCESS);
	mlx_destroy_display(mlx->mlx);
	free(mlx->mlx);
	return (EXIT_SUCCESS);
}
