void	cleanup_mlx(
			t_mlx *mlx)
{
	if (mlx->mlx == NULL)
		return ;
	if (mlx->img)
		mlx_destroy_image(mlx->mlx, mlx->img);
	if (mlx->window)
		mlx_destroy_window(mlx->mlx, mlx->window);
	mlx_destroy(mlx->mlx);
}
